		while i <length-1 and list[i]+1 == list[i + 1]:
			i += 1
		high = list[i]
		if (high - low >= 1):
			yield [low, high]
		elif (high - low == 1):
			yield [low, ]
			yield [high, ]
		else:
			yield [low, ]
		i += 1

